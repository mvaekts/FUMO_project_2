package sample;

public class Point
{


    public Point(double x, double y)
    {
        X = x;
        Y = y;
    }


    private double X;
    private double Y;

    public double getX()
    {
        return X;
    }

    public double getY()
    {
        return Y;
    }

    public void setX(double x)
    {
        X = x;
    }

    public void setY(double y)
    {
        Y = y;
    }



}
